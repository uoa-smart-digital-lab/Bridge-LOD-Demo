using System.Collections;
using System.Collections.Generic;
using UnityEngine;

// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Public functions
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
public interface IXRUX_Keyboard
{
    void Send(XRData theData);
}
// ----------------------------------------------------------------------------------------------------------------------------------------------------------



// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Main class
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
[AddComponentMenu("OpenXR UX/Objects/XRUX Keyboard")]
public class XRUX_Keyboard : MonoBehaviour, IXRUX_Keyboard
{
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Public variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    public bool clearOnSend = false;
    public XRUX_Inputfield inputfield;
    public UnityXRDataEvent onSend;
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Private variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Send on
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    public void Send (XRData theData)
    {
        if (onSend != null) onSend.Invoke(theData);
        if (inputfield != null) inputfield.Clear();
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
}
