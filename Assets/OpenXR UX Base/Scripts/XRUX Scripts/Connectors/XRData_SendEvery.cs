using System.Collections;
using System.Collections.Generic;
using UnityEngine;

// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Public functions
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
public interface IXRData_SendEvery
{
    void Go();
    void Stop();
}
// ----------------------------------------------------------------------------------------------------------------------------------------------------------



// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Main class
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
[AddComponentMenu("OpenXR UX/Connectors/XRData Send Every")]
public class XRData_SendEvery : MonoBehaviour, IXRData_SendEvery
{
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Public variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    public float timeInSeconds = 1.0f;
    public UnityXRDataEvent onChange;
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Private variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    private float lastTime; // Time when last activated.
    private bool running = true;
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Send every time period an event and action
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    void Update()
    {
        if (running)
        {
            if ((Time.time - lastTime) > timeInSeconds)
            {
                if (onChange != null) onChange.Invoke(new XRData(true));
                lastTime = Time.time;
            }
        }
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Start and Stop the function
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    public void Go()
    {
        // Send one event
        if ((onChange != null) && running) onChange.Invoke(new XRData(true));
        // And prepare for the next
        lastTime = Time.time;
        running = true;
    }
    public void Stop()
    {
        running = false;
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
}
