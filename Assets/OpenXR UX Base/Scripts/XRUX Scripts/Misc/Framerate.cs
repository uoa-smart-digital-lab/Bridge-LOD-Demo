using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using TMPro;

// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Main Class
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
public class Framerate : MonoBehaviour
{
    private int numFrames = 10;
    private TextMeshPro textDisplay;
    private float[] frameRates;
    private int frameCounter = 0;

    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Set things up
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    void Start()
    {
        textDisplay = GetComponent<TextMeshPro>();
        frameRates = new float[numFrames];       
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // On every frame update, calculate the framerate and store a number of these for the average calculation, and place it on the text mesh pro object
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    void Update()
    {
        if (textDisplay != null) 
        {
            frameRates[frameCounter] = 1.0f / Time.unscaledDeltaTime;
            frameCounter = (frameCounter + 1) % numFrames;

            textDisplay.text = Average().ToString();
        }
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Calculate averages for the number of frames saved.
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    int Average()
    {
        float sum = 0.0f;
        for (int i=0; i<numFrames; i++)
        {
            sum += frameRates[i];
        }
        return (Mathf.RoundToInt(sum/numFrames));
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
}
